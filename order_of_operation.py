# Order of Operation
# Python mathematic instructions takes BEDMAS Arithmetic operation
# B - Brackets
# E - Exponent
# D - Division
# M - Multiplication
# A - Addition
# S - Subtraction

x = 2
y = 3
answer = 5 - 3 * (x + y)** 2 # first x + y --> 5, then 5 square --> 25, then 25 multiplied by 3 --> 75 and then 5 - 75 --> -70
print(answer)