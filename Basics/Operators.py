# Arithmetic Operators
# + --> Addition
x = 3
y = 2
sum = x + y
print("The sum is: ",sum)

# - --> Subtraction
sub = x - y
print("x minus y is: ",sub)

# * --> Multiplication
product = x * y
print("The product is: ",product)

# / --> Division
division = x / y
print("The division is: ",division)

# % --> Modulus
reminder = x % y
print("The reminder is: ",reminder)

# ** --> Exponent/Power
exponent = x ** y
print("x to the power of y is: ",exponent)
